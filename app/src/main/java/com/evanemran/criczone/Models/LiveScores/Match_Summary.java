package com.evanemran.criczone.Models.LiveScores;

public class Match_Summary {
    String in_play = "";
    String result = "";
    String status = "";
    String toss = "";
    String home_scores = "";
    String away_scores = "";

    public String getIn_play() {
        return in_play;
    }

    public String getResult() {
        return result;
    }

    public String getStatus() {
        return status;
    }

    public String getToss() {
        return toss;
    }

    public String getHome_scores() {
        return home_scores;
    }

    public String getAway_scores() {
        return away_scores;
    }
}
